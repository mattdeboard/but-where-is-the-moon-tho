addEventListener("fetch", event => {
  if (event.request.cf === undefined) {
    event.respondWith(
      new Response(
        JSON.stringify({
          error: "The `cf` object is not available inside the preview.",
        }),
      ),
      { headers: { "content-type": "application/json;charset=UTF-8" } },
    );
  } else {
    event.respondWith(
      handleRequest(event.request).catch(
        err => new Response(err.stack, { status: 500 }),
      ),
    );
  }
});

/**
 * Many more examples available at:
 *   https://developers.cloudflare.com/workers/examples
 * @param {Request} request
 * @returns {Promise<Response>}
 */
async function handleRequest({ cf }) {
  const distance = main({
    lat: parseFloat(cf.latitude),
    long: parseFloat(cf.longitude),
    // Sydney Opera House
    // lat: -33.8568,
    // long: 151.215256,
  });

  if (distance === -1)
    return new Response("Sorry, no ephemerides available for this date.");

  console.log("Reported Distance:", distance / 1000);
  const response = `<!DOCTYPE html>
<body>
  <div>
    The Moon is about ${distance / 1000}* kilometers of travel* from you.
  </div>
  <br />
  <div>
    <div>[*] - Caveats</div>
    <ol>
      <li>
        Using <a href="https://in-the-sky.org/ephemeris.php?ird=1&irs=1&ima=1&iob=1&objtype=1&objpl=Moon&objtxt=Mars&tz=0&startday=24&startmonth=12&startyear=2021&interval=1&rows=25" _target="blank">static ephemeris</a> for lunar coordinates, so it will be off by some degrees. How many degrees depends on time of day, as the ephemeris gives RAsc and Decl per day.
      </li>
      <li>
        "kilometers of travel" is a measurement of how far it would take to travel from your current location to the spot on earth closest to the moon (disregarding altitude), then adding the current distance from the earth to the moon.
      </li>
    </ol>
  </div>
</body>`;
  return new Response(response, {
    headers: {
      "content-type": "text/html;charset=UTF-8",
    },
  });
}

const AVG_LUNAR_ALTITUDE_FROM_EARTH = 384472282; // meters;

function main(visitorPos) {
  const date = new Date();
  const dateKey = `${date.getFullYear()}-${date.getMonth() +
    1}-${date.getDate()}`;
  const ephemera = dateIndexedEphemeris[dateKey];
  if (!ephemera) {
    return -1;
  }

  console.log("Ephemera:", ephemera);
  const declDeg = ephemera.declDeg.startsWith("+")
    ? parseInt(ephemera.declDeg.slice(1))
    : parseInt(ephemera.declDeg);
  const surfaceDistance = distanceFromVisitorToObserverZenith(
    {
      lat: dmsToDecimal({
        hour: declDeg,
        minute: parseInt(ephemera.declMin),
        second: parseInt(ephemera.declSec),
      }),
      long: dmsToDecimal({
        hour: parseInt(ephemera.raHour),
        minute: parseInt(ephemera.raMin),
        second: parseInt(ephemera.raSec),
      }),
    },
    visitorPos,
  );
  return surfaceDistance + AUtoKM(parseFloat(ephemera.distanceInAu)) * 1000;
}

function distanceFromVisitorToObserverZenith(obsZenithPos, visitorPos) {
  const earthRadius = 6371e3; // meters
  const lat1Rad = (obsZenithPos.lat * Math.PI) / 180;
  const lat2Rad = (visitorPos.lat * Math.PI) / 180;
  const latDelta = ((lat2Rad - lat1Rad) * Math.PI) / 180;
  const longDelta = ((visitorPos.long - obsZenithPos.long) * Math.PI) / 180;
  // This formula from https://www.movable-type.co.uk/scripts/latlong.html
  const d =
    Math.acos(
      Math.sin(lat1Rad) * Math.sin(lat2Rad) +
        Math.cos(lat1Rad) * Math.cos(lat2Rad) * Math.cos(longDelta),
    ) * earthRadius;
  return d;
}

function AUtoKM(dist) {
  return dist * 149597870.691;
}

function dmsToDecimal(pos) {
  const hour = pos.hour;
  const minute = pos.minute / 60;
  const second = pos.second / 3600;
  const ascensionDecimal = (hour + minute + second) * 15;
  return ascensionDecimal > 180 ? ascensionDecimal - 360 : ascensionDecimal;
}
function decimalToDMS(value) {
  const degrees = value - (value % 1);
  const minuteFloat = (value % 1) * 60;
  const minutes = minuteFloat - (minuteFloat % 1);
  const seconds = (minuteFloat % 1) * 60;
  return {
    degrees,
    minutes,
    seconds,
  };
}

